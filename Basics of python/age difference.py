# getting the ages from input funcion and assigning the values
my_age = input("Enter your age: ")
neighbor_age = input("Enter the age of the neighbor: ")

# calculating the difference between ages
age_difference = int(my_age) - int(neighbor_age)

# printing all the variables
# function print can print unlimited amount of values,
# so we can pass different arguments
print("While my age is", my_age)
# or we can use special strings
print(f"Neighbor age is {neighbor_age}")
# or we can add 2 values together and get the same result
print("The difference is " + str(age_difference))









